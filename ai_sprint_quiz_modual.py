# --- [3. 핵심 검색 함수 (TraceState 규격과 연동)] ---
def get_quiz_from_chroma(data: dict) -> dict:
    """
    팀원들과 약속한 input (data) 예시:
    {"selected": ["일차함수", "이차함수"]}

    반환 형태:
    {"quiz": [QuizItem, QuizItem, ...]}
    """
    selected_concepts = data.get("selected", [])
    quiz_list: list[QuizItem] = []

    for concept in selected_concepts:
        # DB에서 선택된 개념 키워드로 유사 문제 2개씩 검색
        results = collection.query(
            query_texts=[concept],
            n_results=2
        )

        documents = results.get('documents', [[]])[0]
        ids = results.get('ids', [[]])[0]
        metadatas = results.get('metadatas', [[]])[0]

        for i in range(len(documents)):
            meta = metadatas[i] if i < len(metadatas) else {}

            # 핵심: 약속된 QuizItem 스키마에 정확히 매칭해서 생성
            quiz_item = QuizItem(
                quiz_id=ids[i] if i < len(ids) else f"q_{i}",  # 채점용 ID
                question=documents[i],
                answer=meta.get("answer", "정답없음"),
                concept=meta.get("concept", concept)
            )
            quiz_list.append(quiz_item)

    return {"quiz": quiz_list}


