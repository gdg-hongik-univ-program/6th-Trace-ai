import os
import glob
import json
import chromadb
from pydantic import BaseModel

# --- [데이터 스키마 (Pydantic)] ---
class Prerequisite(BaseModel):
    name: str
    chapter: str | None = None
    depth: int = 1

class QuizItem(BaseModel):
    quiz_id: str          # 채점할 때 매칭용 
    question: str
    answer: str           # 정답 
    concept: str          # 어떤 개념 확인용인지

class GradeResult(BaseModel):
    quiz_id: str
    is_correct: bool
    feedback: str

class TraceState(BaseModel):
    question: str
    concepts: list[str] = []
    prerequisites: list[Prerequisite] = []
    selected: list[str] = []
    explanation: str = ""
    quiz: list[QuizItem] = []
    user_answers: dict[str, str] = {}    # {quiz_id: 학생 답}
    grade_results: list[GradeResult] = []


